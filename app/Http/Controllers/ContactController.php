    /**
     * this function stores the contact us information to the contact database
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:30',
            'email' => 'required|string|email|max:100',
            'phone' => 'required|string|max:14',
            'body' => 'required|string',
            // 'g-recaptcha-response' => 'required|captcha',
        ]);

        Contact::create([ 
            'name' => utf8_encode($request->input('name')),  
            'email' => utf8_encode($request->input('email')),  
            'phone' => utf8_encode($request->input('phone')),  
            'body' => utf8_encode($request->input('body')),    
        ]);

        return back()->with([
            'success_message' => 'Your Message have been sent successfully!',
        ]);
    }

